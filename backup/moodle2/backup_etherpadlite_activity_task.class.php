/**
 * podcaster backup task that provides all the settings and steps to perform one
 * complete backup of the activity
 */
class backup_etherpadlite_activity_task extends backup_activity_task {
 
    /**
     * Define (add) particular settings this activity can have
     */
    protected function define_my_settings() {
        // No particular settings for this activity
    }
 
    /**
     * Define (add) particular steps this activity can have
     */
    protected function define_my_steps() {
        // podcaster only has one structure step
        $this->add_step(new backup_etherpadlite_activity_structure_step('etherpadlite_structure', 'etherpadlite.xml'));
    }
 
    /**
     * Code the transformations to perform in the activity in
     * order to get transportable (encoded) links
     */
    static public function encode_content_links($content) {
        global $CFG;

        // No links to encode
        return $content;

   }
}